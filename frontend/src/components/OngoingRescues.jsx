import { useState } from "react";
import { FaAmbulance } from "react-icons/fa";
import { AiOutlinePrinter } from "react-icons/ai";
import jsPDF from "jspdf";
import "jspdf-autotable";

const OngoingRescues = ({ requests, user }) => {
  const ongoingRescues = requests
    .filter((request) => request.status === "assigned")
    .map((request, index) => ({
      id: index + 1,
      location: request.location.address,
      rescuer: `${request.rescuerId}`,
      status: request.status,
      acceptedTimestamp: new Intl.DateTimeFormat("en-US", {
        year: "numeric",
        month: "2-digit",
        day: "2-digit",
        hour: "2-digit",
        minute: "2-digit",
        second: "2-digit",
      }).format(new Date(request.acceptedTimestamp)),
      estimatedArrivalTime: "TBD",
      estimatedDepartureTime: "TBD",
    }));

  const [showMap, setShowMap] = useState(false);
  const [currentPage, setCurrentPage] = useState(1);
  const rowsPerPage = 5;

  // Paginate data
  const totalRows = ongoingRescues.length;
  const totalPages = Math.ceil(totalRows / rowsPerPage);
  const startIndex = (currentPage - 1) * rowsPerPage;
  const endIndex = startIndex + rowsPerPage;
  const paginatedRescues = ongoingRescues.slice(startIndex, endIndex);

  const handlePageChange = (newPage) => {
    if (newPage > 0 && newPage <= totalPages) {
      setCurrentPage(newPage);
    }
  };

  const pageNumbers = [];
  for (let i = 1; i <= totalPages; i++) {
    pageNumbers.push(i);
  }

  const visiblePages =
    pageNumbers.length <= 5
      ? pageNumbers
      : pageNumbers.slice(
          Math.max(0, currentPage - 2),
          Math.min(totalPages, currentPage + 2)
        );

  const handleShowMap = (location) => {
    setShowMap(location);
  };

  // Print report function
  const handlePrint = () => {
    const doc = new jsPDF("landscape");

    // Title
    doc.setFontSize(18);
    doc.text("Ongoing Rescues Report", 10, 10);

    // Define the columns and rows for the table
    const tableColumn = [
      { title: "#", dataKey: "id" },
      { title: "Rescuer ID", dataKey: "rescuer" },
      { title: "Location", dataKey: "location" },
      { title: "Accepted Timestamp", dataKey: "acceptedTimestamp" },
      { title: "Estimated Arrival", dataKey: "estimatedArrivalTime" },
      { title: "Estimated Departure", dataKey: "estimatedDepartureTime" },
      { title: "Status", dataKey: "status" },
    ];

    const tableRows = ongoingRescues.map((rescue) => ({
      id: rescue.id,
      rescuer: rescue.rescuer,
      location: rescue.location,
      acceptedTimestamp: rescue.acceptedTimestamp,
      estimatedArrivalTime: rescue.estimatedArrivalTime,
      estimatedDepartureTime: rescue.estimatedDepartureTime,
      status: rescue.status,
    }));

    // Table styling and rendering
    doc.autoTable({
      head: [tableColumn.map((col) => col.title)],
      body: tableRows.map((row) => Object.values(row)),
      startY: 40, // Starting Y position for the table
      theme: "grid",
      styles: {
        fontSize: 10,
        cellPadding: 2,
        halign: "center",
        valign: "middle",
        lineColor: "#557C55",
        lineWidth: 0.5,
      },
      headStyles: {
        fillColor: "#557C55",
        textColor: "#FFFFFF",
        fontSize: 10,
      },
    });

    // Add footer
    const pageHeight = doc.internal.pageSize.getHeight();
    const pageWidth = doc.internal.pageSize.getWidth();
    const footerY = pageHeight - 10; // Position 10 units from the bottom

    doc.setFontSize(10);
    // "Generated by" on the left side
    doc.text(
      `Generated by: ${user?.first_name} ${user?.last_name}`,
      10,
      footerY
    );
    // "Generated date" on the right side
    doc.text(
      `Generated date: ${new Date().toLocaleString()}`,
      pageWidth - 70, // Adjust based on the text width to align right
      footerY
    );

    // Save the PDF
    doc.save("ongoing_rescue_operations.pdf");
  };

  return (
    <div className="flex-1 p-2 sm:p-3 lg:p-4 h-full flex flex-col bg-gray-50">
      {/* Header */}
      <div className="flex items-center mb-4 sm:mb-6 pb-2 sm:pb-4 border-b border-gray-200">
        <FaAmbulance className="text-xl sm:text-3xl text-[#557C55] mr-2 sm:mr-3" />
        <h4 className="text-lg sm:text-2xl font-semibold text-[#557C55]">
          Ongoing Rescue Operations
        </h4>
      </div>

      <p className="mb-4 text-sm sm:text-md text-gray-600">
        Monitoring the status and progress of active rescue efforts.
      </p>

      {/* Map Toggle */}
      {showMap && (
        <div className="mb-4 sm:mb-6">
          <h5 className="text-lg sm:text-2xl font-semibold mb-2 text-primary">
            <p className="text-secondary">Location Map:</p>
            {showMap}
          </h5>
          <div
            className="map-placeholder bg-[#eaeaea] rounded-lg"
            style={{ height: "200px" }}
          >
            <p className="text-center pt-4 text-sm sm:text-base">
              Map displaying location: {showMap}
            </p>
          </div>
          <button
            className="bg-[#FA7070] text-white px-3 py-1 sm:px-4 sm:py-2 rounded-md mt-2 hover:bg-[#ff4444] transition text-xs sm:text-sm"
            onClick={() => setShowMap(false)}
          >
            Hide Map
          </button>
        </div>
      )}

      {/* Rescue Data Table */}
      <div className="rounded-lg p-2 sm:p-4 flex flex-col flex-1">
        {/* Print Button */}
        <div className="flex justify-end mb-4">
          <button
            onClick={handlePrint}
            className="bg-primary-medium text-white px-2 py-1 sm:px-3 sm:py-2 rounded-lg text-xs hover:bg-[#6EA46E] transition mt-2 sm:mt-0 flex items-center"
          >
            <AiOutlinePrinter className="text-sm" />
            Generate
          </button>
        </div>
        <div className="overflow-x-auto">
          <table className="min-w-full text-xs sm:text-sm rounded-lg border border-primary-medium">
            <thead className="bg-primary-medium">
              <tr>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-medium text-warning border border-gray-300">
                  #
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  RID
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  Location
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  Accepted Timestamp
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  Distance
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  ETA
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  Status
                </th>
                <th className="px-2 sm:px-4 py-2 sm:py-3 text-sm font-semibold text-white border border-gray-300">
                  Map
                </th>
              </tr>
            </thead>
            <tbody>
              {paginatedRescues.map((rescue, index) => (
                <tr
                  key={rescue.id}
                  className={`border-b text-center ${
                    index % 2 === 0 ? "bg-white" : "bg-gray-100"
                  }`}
                >
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.id}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.rescuer}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.location}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.acceptedTimestamp}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.estimatedArrivalTime}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.estimatedDepartureTime}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    {rescue.status}
                  </td>
                  <td className="px-2 sm:px-4 py-1 sm:py-2 border border-gray-300">
                    <button
                      onClick={() => handleShowMap(rescue.location)}
                      className="bg-[#557C55] text-white px-2 py-1 rounded-md text-xs hover:bg-[#6EA46E] transition"
                    >
                      View Map
                    </button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>

        {/* Pagination */}
        <div className="flex justify-between items-center mt-4">
          <button
            onClick={() => handlePageChange(currentPage - 1)}
            disabled={currentPage === 1}
            className="bg-primary-medium text-white px-3 py-1 rounded-md text-xs"
          >
            Previous
          </button>

          <div>
            {visiblePages.map((number) => (
              <button
                key={number}
                onClick={() => handlePageChange(number)}
                className={`mx-1 px-2 py-1 rounded-md text-xs ${
                  number === currentPage
                    ? "bg-primary-medium text-white"
                    : "bg-gray-200 text-black"
                }`}
              >
                {number}
              </button>
            ))}
          </div>

          <button
            onClick={() => handlePageChange(currentPage + 1)}
            disabled={currentPage === totalPages}
            className="bg-primary-medium text-white px-3 py-1 rounded-md text-xs"
          >
            Next
          </button>
        </div>
      </div>
    </div>
  );
};

export default OngoingRescues;
